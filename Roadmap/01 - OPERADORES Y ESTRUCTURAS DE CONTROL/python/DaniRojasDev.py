'''
Operadores aritméticos
'''

a = 10
b = 3

print (f"10 + 4 = {a + b}") #suma
print (f"10 - 4 = {a - b}")#resta
print (f"10 x 4 = {a * b}") #multiplicación
print (f"10 / 4 = {a / b}") #división
print (f"10 % 4 = {a % b}")#módulo
print (f"10 ** 4 = {a ** b}") #exponente
print (f"10 // 4 = {a // b}") #cociente

