public class AriasLopez {

    public static void main(String[] args) {

        // VARIABLES

        int numero1 = 2;
        int numero2 = 5;
        double resultado = numero1 + numero2;

        /*
         * OPERADORES
         */
        System.out.println("Esta es una suma: " + (2 + 5) + "Resultado: " + resultado);
        System.out.println("Esta es una resta: " + (2 - 5) + "Resultado: " + resultado);
        System.out.println("Esta es una multiplicacion: " + (2 * 5) + "Resultado: " + resultado);
        System.out.println("Esta es una division: " + (2 / 5) + "Resultado: " + resultado);
        System.out.println("Esto es un modulo: " + (2 % 5) + "Resultado: " + resultado);
    }
}
